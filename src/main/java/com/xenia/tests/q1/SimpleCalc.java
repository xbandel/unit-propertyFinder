package com.xenia.tests.q1;


public class SimpleCalc {
    public double add(double opL, double opR){
        return opL + opR;
    }

    public double subtract(double opL, double opR){
        return opL - opR;
    }

    public double multiply(double opL, double opR){
        return opL * opR;
    }

    public double divide(double opL, double opR){
        return opL / opR;
    }
}
